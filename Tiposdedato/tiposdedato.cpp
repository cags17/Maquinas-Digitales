#include <iostream>
using namespace std;
int main(){
    cout << "Tamaño de bytes de <char>"<< sizeof(char) << endl;
    cout << "Tamaño de bytes de <bool>"<< sizeof(bool) << endl;
    cout << "Tamaño de bytes de <int>"<< sizeof(int) << endl;
    cout << "Tamaño de bytes de <float>"<< sizeof(float) << endl;
    cout << "Tamaño de bytes de <long>"<< sizeof(long) << endl;
    cout << "Tamaño de bytes de <double>"<< sizeof(double) << endl;
    return 0;
}